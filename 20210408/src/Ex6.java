
public class Ex6 {

	public static void main(String[] args) {
		//  논리형 리터널 true, false
		boolean b = true;
		System.out.println(b);
		System.out.println(!b);
		System.out.println(!!b);
		boolean b1 = false;
		System.out.println(b1);
		System.out.println(!b1);
		
		System.out.println(!true);
		System.out.println(!false);
	}
}
