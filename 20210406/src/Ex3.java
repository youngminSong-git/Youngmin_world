
public class Ex3 {
	public static void main(String [] args) {
		// 정수형변수 
		byte b;  // -128 ~ 127   1byte : 8bit
		b = -128; 
		System.out.println(b);
		b = 127;
		System.out.println(b);
		char c; // 0 ~ 65535 // unicode 2byte 
		c = 0;
		System.out.println(c);
		c= 65535;
		System.out.println(c);
		short s; // 2byte
		int i ; // -21000000 ~ 21000000  4byte
		i = 1;
		long l; //  8byte
		l = 10000000l;
		c = 65;
		System.out.println(c);
		/// 문자를 숫자로 변환한 값
		c = 97;
		System.out.println(c);
		c = 66;
		System.out.println(c);
		c = 98;
		System.out.println(c);
		c = 'D'; // char만이 문자와 숫자를 저장할 수 있다.
		System.out.println(c);
		int ii = c;
		System.out.println(ii);
		
		/// 정수, 실수, 문자, 문자열, 논리 : 자료형
		// 비지니스 자료형
		// 2장이 제일 중요
		// 3장이 다음
		// 4장이 그 다음 
		// 2~4장만 잘하면 프로그램은 어렵지 않다.
		
		
	}
}
