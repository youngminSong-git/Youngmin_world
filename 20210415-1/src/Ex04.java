/// 객체 지향의 특징 : 캡슐화, 다향성
public class Ex04 {
	// 멤버필드를 메서드 사용
	// 멤버 필드
	private int age;
	private String name;
	// 메소드
	public void setAge(int age) {
		this.age = age;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public String getName() {
		return name;
	}
}
