
public class Ex5 {

	public static void main(String[] args) {
		// 반복문
		System.out.println("1번째입니다.");
		System.out.println("2번째입니다.");
		System.out.println("3번째입니다.");
		System.out.println("4번째입니다.");
		System.out.println("5번째입니다.");
		System.out.println("6번째입니다.");
		System.out.println("7번째입니다.");
		System.out.println("8번째입니다.");
		System.out.println("9번째입니다.");
		System.out.println("10번째입니다.");
		
		System.out.println("======= 반복문 사용 =====");
		
		for(int i = 1 ; i <= 10 ; i++) {
			System.out.println(i + "번째입니다.");
		}
		
	}

}
