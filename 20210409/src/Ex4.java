public class Ex4 {
	public static void main(String[] args) {
		// 제어문 :조건문 (if,switch), 반복문(for, while)
		// 조건문 : 선택된 것만 사용할 수 있게 제어
		int money = 3000;
		if (money >= 3000) {
			System.out.println("차를 타고 가시오.");
		}
		System.out.println("if문이 끝났습니다.");
		
		// 단일 if 
		money = 2000;
		    // 조건식(condition)
		if (money >= 3000) { //조건문
			System.out.println("차를 타고 가시오.");
		}
		System.out.println("if문이 끝났습니다.");
	}
}
